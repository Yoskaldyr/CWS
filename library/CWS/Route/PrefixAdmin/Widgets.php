<?php

/**
 * Route prefix handler for widgets in the admin control panel.
 *
 * @package XenForo_Widgets
 */
class CWS_Route_PrefixAdmin_Widgets implements XenForo_Route_Interface
{
	/**
	 * Match a specific route for an already matched prefix.
	 *
	 * @see XenForo_Route_Interface::match()
	 */
	public function match($routePath, Zend_Controller_Request_Http $request, XenForo_Router $router)
	{
		$action = $router->resolveActionWithStringParam($routePath, $request, 'widget_id');
		return $router->getRouteMatch('CWS_ControllerAdmin_Widget', $action, 'cwsWidgets');
	}

	/**
	 * Method to build a link to the specified page/action with the provided
	 * data and params.
	 *
	 * @see XenForo_Route_BuilderInterface
	 */
	public function buildLink($originalPrefix, $outputPrefix, $action, $extension, $data, array &$extraParams)
	{
		return XenForo_Link::buildBasicLinkWithStringParam($outputPrefix, $action, $extension, $data, 'widget_id');
	}
}